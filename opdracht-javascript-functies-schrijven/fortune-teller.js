let tellFortune = function(numChildren, partner, location, jobTitle) {
  return "You will be a " + jobTitle + " in " + location + ", and married to " + partner + " with " + numChildren + " kids."
}

console.log(tellFortune(2, "Sarah Smith", "Hawaii", "Accountant"));
console.log(tellFortune(0, "Diana Lovely", "France", "Writer"));
console.log(tellFortune(1, "Julie Westminster", "Berlin", "Developer"));